#define LEDOUT (*((volatile uint8_t *)(0x42098040)))

// Transmit strings to your PC using UART0.
void Program11_1(void) {

    Clock_Init48MHz();  // set system clock to 48 MHz
    LaunchPad_Init();
    UART0_Init();       // Initialize UART0 that is connected to the USB port.

    LEDOUT = 1;

    UART0_OutString("--- RSLK 1.1 --------\n\r");
    UART0_OutString("1234567890\n\r");
    UART0_OutString("abcdefghijklmnopqrstuvwxyz\n\r");
    UART0_OutString("ABCDEFGHIJKLMNOPQRSTUVWXYZ\n\r");
    UART0_OutString("!@#$%^&*()_+-=\n\r");
    UART0_OutString(",./<>?;'\"[]\\{}|\n\r");

    uint32_t data=0;

    while(1){
        UART0_OutString("\n\rdata = ");
        UART0_OutUDec(data);
        data = data+27;
        Clock_Delay1ms(1000);
        LEDOUT = ~LEDOUT;
    }
}


