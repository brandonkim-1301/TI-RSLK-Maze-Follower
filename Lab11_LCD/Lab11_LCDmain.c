// Print characters to the Nokia 5110 48x84 LCD.
// The string will automatically wrap, so padding spaces may
// be needed to make the output look optimal.
void Program11_3(void){

    Clock_Init48MHz();                   // set system clock to 48 MHz
    Nokia5110_Init();
    LaunchPad_Init();

    // Contrast value 0xB1 looks good on red SparkFun
    // Adjust this from 0xA0 (lighter) to 0xCF (darker) if necessary.
    uint8_t const contrast = 0xB1;
    Nokia5110_SetContrast(contrast);

    LEDOUT = 1;

    for(uint8_t count = 0; count < 36; count++) {
        // three step process: clear, build, display
        Nokia5110_ClearBuffer();
        Nokia5110_PrintBMP(count, 47, af, 0);
        Nokia5110_DisplayBuffer();
        Clock_Delay1ms(60);                // delay ~0.06 sec at 48 MHz
    }

    for(uint8_t count = 35; count > 0; count--) {
        // three step process: clear, build, display
        Nokia5110_ClearBuffer();
        Nokia5110_PrintBMP(count, 47, af, 0);
        Nokia5110_DisplayBuffer();
        Clock_Delay1ms(60);                // delay ~0.04 sec at 48 MHz
    }

    for(uint8_t count = 0; count < 18; count++) {
        // three step process: clear, build, display
        Nokia5110_ClearBuffer();
        Nokia5110_PrintBMP(count, 47, af, 0);
        Nokia5110_DisplayBuffer();
        Clock_Delay1ms(60);                // delay ~0.04 sec at 48 MHz
    }

    Clock_Delay1ms(100);                 // delay 1 sec at 48 MHz

    Nokia5110_Clear();
    Nokia5110_OutString("************* LCD Test *************Letter: Num:----------- ");
    Nokia5110_OutChar(127);                    // print AF sign

    while(1){
        for(int i = 0; i < 26; i++){
            Nokia5110_SetCursor(4, 5);         // five leading spaces, bottom row
            Nokia5110_OutChar((i%26)+'A');
            Nokia5110_OutChar(' ');
            Nokia5110_OutUDec(1<<(i&0x0F));
            Clock_Delay1ms(500);               // delay ~0.5 sec at 48 MHz
            LEDOUT = ~LEDOUT;
        }

        for(int i = 0; i < 26; i++){
            Nokia5110_SetCursor(5, 5);         // five leading spaces, bottom row
            Nokia5110_OutSDec(-(1<<(i&0x0F)));
            Clock_Delay1ms(500);               // delay ~0.5 sec at 48 MHz
            LEDOUT = ~LEDOUT;
        }
    }
}


